    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'title' => 'required|string',
            'description' => 'required|string',
            'metatags' => 'required|string',
            
        ]);
        // dd($request);
        try {
            $old_homepage = HomePage::find($request->homepage);
            if (HomePage::updateOrCreate(['id' => ($old_homepage->id ?? 0)], $request->all()))
                Session::flash('status', 'homepage details Uploaded Successfully');
            else
                Session::flash('error', 'Unable to upload homepage info.');

            return redirect()->back();
        } catch (Exception $ex) {

            return redirect()->back()->with('error', 'something went wrong! ' . $ex->getMessage());
        }
    }

